import "C"
import "unsafe"

func fillScratchFast(key [32]byte, scratch []uint64) bool {
	if len(scratch) == 0 {
		return true
	}
	C.nm_fill_scratch_arm64(
		(*C.uint8_t)(unsafe.Pointer(&key[0])),
		(*C.uint64_t)(unsafe.Pointer(&scratch[0])),
		C.size_t(len(scratch)),
	)
	return true
}


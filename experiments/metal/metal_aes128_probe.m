static void expandAES128Key(const uint8_t key[16], uint8_t roundKeys[176]) {
    static const uint8_t rcon[11] = {
        0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36
    };
    memcpy(roundKeys, key, 16);
    uint8_t temp[4];
    int bytes = 16;
    int round = 1;
    while (bytes < 176) {
        for (int i = 0; i < 4; i++) temp[i] = roundKeys[bytes - 4 + i];
        if ((bytes % 16) == 0) {
            uint8_t first = temp[0];
            temp[0] = kSbox[temp[1]] ^ rcon[round++];
            temp[1] = kSbox[temp[2]];
            temp[2] = kSbox[temp[3]];
            temp[3] = kSbox[first];
        }
        for (int i = 0; i < 4; i++) {
            roundKeys[bytes] = roundKeys[bytes - 16] ^ temp[i];
            bytes++;
        }
    }
}

